-- Откат миграции 000013.
--
-- Автоматически удалить запись, добавленную up-миграцией, аккуратно нельзя:
-- up использует динамический max(version)+1, и к моменту отката могли быть
-- опубликованы более новые версии через админ-UI. Удалять max(version) опасно —
-- можно снести чью-то правку.
--
-- Идентифицируем нашу запись по специфическому маркеру в контенте — упоминанию
-- одновременно target_categories (массив) и meal_structure, которых нет в
-- prompt''ах до миграции 000013. Это безопасно: даже если admin опубликовал
-- v3 через UI, эти маркеры останутся уникальны для нашей версии.

DELETE FROM prompts
WHERE name = 'classification'
  AND content LIKE '%target_categories%'
  AND content LIKE '%meal_structure%';
