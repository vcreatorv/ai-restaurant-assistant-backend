-- Роль категории в RAG-пайплайне рекомендаций:
--   main      — «основные» категории, по которым делается диверсификация main-выдачи
--                (на широком запросе «что покушать» добавляем top-1 из непокрытых main-категорий).
--   companion — категории-сопровождение (соус/гарнир/закуска/десерт/напиток),
--                по 1 блюду на запрос; пропускаются, если main уже содержит блюдо из этой категории.
--   none      — категория не участвует в diversify/companion-логике (учитывается только обычным retrieval'ом).
--
-- Список ролей замораживался в configs/config.yaml строками, что ломало диверсификацию при переименовании
-- категорий в админке. Переезжаем в БД, чтобы admin мог менять роли через админку, и чтобы не было
-- сравнения по name string в RAG-коде.

ALTER TABLE categories
    ADD COLUMN role text NOT NULL DEFAULT 'none'
        CHECK (role IN ('none', 'main', 'companion'));

CREATE INDEX idx_categories_role ON categories(role) WHERE role <> 'none';

-- Сидируем актуальные роли текущим категориям. Опираемся на name, потому что id seed-зависимые.
-- Если категория переименована/удалена — обновлять руками в админке после миграции.
UPDATE categories SET role = 'main'      WHERE name IN (
    'Супы',
    'Салаты',
    'Мясное',
    'Морепродукты',
    'Стейки',
    'На гриле',
    'Бургеры',
    'Пицца и паста'
);

UPDATE categories SET role = 'companion' WHERE name IN (
    'Соусы',
    'Гарниры',
    'Закуски',
    'Десерты',
    'Напитки'
);
