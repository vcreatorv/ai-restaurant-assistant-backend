def main() -> int:
    repo_root = Path(__file__).resolve().parent.parent
    menu_path = repo_root / "seed" / "menu.json"

    with menu_path.open("r", encoding="utf-8") as f:
        data = json.load(f)

    dishes = data["dishes"]
    updated = 0
    missing_in_dict = []
    extra_in_dict = set(DESCRIPTIONS.keys())

    for dish in dishes:
        name = dish["name"]
        desc = DESCRIPTIONS.get(name)
        if desc is None:
            missing_in_dict.append(name)
            continue
        extra_in_dict.discard(name)
        if dish.get("description") != desc:
            dish["description"] = desc
            updated += 1

    if missing_in_dict:
        print("WARN: dishes from menu.json missing in DESCRIPTIONS:", file=sys.stderr)
        for name in missing_in_dict:
            print(f"  - {name}", file=sys.stderr)
    if extra_in_dict:
        print("WARN: keys in DESCRIPTIONS not present in menu.json:", file=sys.stderr)
        for name in sorted(extra_in_dict):
            print(f"  - {name}", file=sys.stderr)

    with menu_path.open("w", encoding="utf-8", newline="\n") as f:
        json.dump(data, f, ensure_ascii=False, indent=2)
        f.write("\n")

    print(f"changed: {updated}")
    print(f"total dishes in menu.json: {len(dishes)}")
    print(f"total keys in DESCRIPTIONS: {len(DESCRIPTIONS)}")
    return 0 if not missing_in_dict and not extra_in_dict else 1


if __name__ == "__main__":
    sys.exit(main())
