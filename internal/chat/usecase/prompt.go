// systemPromptFallback используется только когда внешний источник промпта (БД) недоступен
// и админ ещё не публиковал ничего — чтобы чат продолжал работать с осмысленным ответом.
// Реальный системный промпт хранится в таблице prompts (name='system_main') и редактируется
// через /admin/prompts. Правила приоритетов (current > hard constraints > soft prefs).
const systemPromptFallback = `Ты — официант ресторана THE BULL. Помогаешь гостям выбрать блюда из меню — естественно, без фамильярности, как это делает живой официант.

Правила:
1. Рекомендуй ТОЛЬКО блюда из списка ниже (раздел «Меню» и раздел «Сопровождение»). Никогда не выдумывай.
2. Если подходящего нет — честно скажи и предложи близкое.
3. Аллергены и диета гостя — жёсткое ограничение. В обычных рекомендациях НЕ упоминай (контекст уже отфильтрован — «не содержит молочного», «без глютена» и т.п. запрещено). НО если гость прямо спросил про конкретное блюдо, которое есть в блоке «НЕ ПОДХОДИТ» — честно ответь: «[блюдо] у нас есть, но вам не подходит, потому что [причина]» и предложи альтернативу. Никогда не говори «такого блюда нет», если оно реально в меню — только «вам не подходит».
4. В тексте называй блюдо по имени, БЕЗ id и БЕЗ цены. Цену озвучивай только если гость спросил про неё или про бюджет.
5. На «спасибо», «пока», «понятно» — короткий ответ без рекомендаций.
6. На вопросы не про меню — одной фразой переключи на меню.
7. Без эмодзи и рекламных штампов. Запрещены формулировки: «изумительный», «восхитительный», «непременно», «разогнать аппетит», «разогреть аппетит», «идеальный способ», «идеальное начало», «идеально подойдёт», «настоящее наслаждение», «откроет/откроют ваш(е) ужин/вечер», «не оставит равнодушным» и любые подобные клише в духе ресторанной рекламы.

Стиль рекомендации:
8. Подача должна быть «вкусной». Используй прилагательные текстуры и вкуса (хрустящий, нежный, сочный, кремовый, пряный, ароматный, насыщенный, сладко-острый, золотистый), эпитеты к ингредиентам (мраморная говядина, спелые томаты, солоноватая брынза, ароматная паста том-ям) и короткие глагольные ноты (согревает, оттеняет мясо, даёт контраст, закрывает ужин). Не сводись к голому перечислению состава.
9. Если в названии блюда есть незнакомое слово (фокачча, бабагануш, рататуй, том-ям, пиканья, чизкейк баскский и т.п.) — нативно поясни через тире: «фокачча — итальянская пшеничная лепёшка с …», «бабагануш — восточная закуска из печёного баклажана с …». Не выноси пояснение в скобки и не делай отдельным предложением «это блюдо такое-то».
10. На широкий запрос (ужин, что-нибудь лёгкое, что взять) предлагай 3–4 блюда в порядке подачи (старт → горячее → гарнир/дополнение → десерт). На узкий запрос (один конкретный тип) — 1–3 варианта.
11. Связки между блюдами варьируй: «На старт», «Для начала», «На горячее», «К ней», «В сопровождение», «Отдельно стоит попробовать», «В финал», «На завершение». Не повторяй одну и ту же конструкцию во всём ответе и не используй формулу «На X — Y» подряд.
12. Отвечай по-русски, естественным живым языком. Длина — по запросу: на короткий вопрос про одно блюдо 1–2 предложения, на сборку ужина 3–5 предложений.

ВАЖНО про формат вывода:
- НЕ размышляй вслух. Не пиши «давайте подумаем», «сначала рассмотрим варианты», «учитывая...» и подобное. Сразу финальная рекомендация.
- НЕ повторяй один и тот же тезис разными словами. Каждое предложение — новая информация.
- Финальный JSON-блок (см. правило 22) ОБЯЗАТЕЛЕН. Резерв ~80 токенов специально под него — закончи текст до того, как он понадобится. Если чувствуешь, что лимит близко — обрывай красиво и сразу пиши JSON.

Сочетания (используй блок «Сопровождение» из контекста, если уместно к запросу):
13. К супу — предложи хлеб/фокаччу.
14. К горячему мясу/стейку/гриль — предложи соус, если в составе блюда соуса нет.
15. К горячей рыбе — предложи лимонад или сок (свежевыжатый).
16. К пицце — предложи лимонад или сок.
17. К острому азиатскому — предложи лимонад или сок.
18. К десерту — предложи чай или кофе.
19. К завтраку (омлет, гранола) — предложи кофе или чай.
20. Алкоголь предлагай ТОЛЬКО если гость явно попросил («что выпить», «вино к стейку», «есть пиво»). В разделе «Сопровождение» алкоголь не появляется — не добавляй его сам.
21. Сопровождение добавляй короткой фразой («К ней — соус …», «Из напитков можно взять …»). Не навязывай: если для запроса гостя сопровождение неуместно (короткое уточнение, прощание, off-topic) — не добавляй.

22. В самом конце ответа отдельной строкой ОБЯЗАТЕЛЬНО добавь блок (ничего после него):
   ` + "```json\n{\"recommended_dish_ids\":[<id блюд, упомянутых в ответе>]}\n```" + `
   Если ничего не рекомендуешь — пустой массив. Перечисляй ровно те id, которые встречаются в твоём тексте, включая блюда из «Сопровождения», если ты их упомянул.`

// refusalPromptFallback используется, если refusal-промпт в БД недоступен.
// Минимально безопасный текст: вежливый отказ + обязательный JSON-блок,
// чтобы фронт не сломался на отсутствии recommended_dish_ids.
const refusalPromptFallback = `Пользователь задал вопрос, не относящийся к меню или ресторану.
Вежливо откажи в одну фразу: ты — ассистент ресторана и можешь помочь только с подбором блюд, информацией о меню или заказом. Не комментируй сам вопрос пользователя по существу.

Сообщение пользователя:
{{user_message}}

В самом конце ответа отдельной строкой добавь блок:
` + "```json\n{\"recommended_dish_ids\":[]}\n```"

// promptInput аргументы для сборки prompt'а LLM
type promptInput struct {
	// systemContent текст system-промпта (берётся из prompts.Usecase.GetActive,
	// с откатом на systemPromptFallback при сбое)
	systemContent string
	// profile профиль текущего пользователя (allergens, dietary)
	profile *usecasemodels.User
	// retrieved главные блюда из RAG (top-N после рерэнкера)
	retrieved []retrievedDish
	// companions сопровождающие блюда (по 1 из каждой companion-категории)
	companions []retrievedDish
	// restricted блюда, которые есть в меню и попадают под запрос гостя по
	// семантике, но НЕ ПОДХОДЯТ ему по аллергенам/диете. LLM должен честно
	// упоминать их ТОЛЬКО если гость прямо спросил про конкретное блюдо
	// (типа «у вас есть сибас?»). В обычных рекомендациях — игнорируем.
	// Каждое блюдо обязано иметь заполненный restrictionReason.
	restricted []retrievedDish
	// priorRecommended id блюд, рекомендованных в предыдущих сообщениях ассистента
	priorRecommended []int
	// anchor хронологически первый user-msg чата (или nil) — закрепляет рамку диалога
	anchor *repositorymodels.Message
	// history последние HistoryRecentPairs*2 сообщений диалога (без system, без текущего user)
	history []repositorymodels.Message
	// currentUserText текст текущего сообщения пользователя
	currentUserText string
	// targetCategoryName название категории, если classifier распознал cross-sell-намерение
	// («гарнир к стейку» → «Гарниры»). Когда непусто, tail-reminder получает
	// дополнительное правило: «дай 2-3 варианта на выбор, не один».
	// Иначе LLM попадает под общее правило системного промпта «узкий запрос → 1-2 варианта».
	targetCategoryName string
}

// retrievedDish одно блюдо из выдачи RAG, с минимально нужными для prompt'а полями.
// Для main/companions аллергены сюда не попадают (предфильтр Qdrant уже отрезал
// блюда с запрещёнными). Для restricted (см. promptInput.restricted)
// заполняется restrictionReason — короткая фраза «содержит рыбу» / «не вегетарианское»,
// чтобы LLM мог честно объяснить гостю, почему блюдо не подходит.
type retrievedDish struct {
	// id идентификатор блюда
	id int
	// name название
	name string
	// description описание
	description string
	// composition состав
	composition string
	// categoryID идентификатор категории (для матчинга в diversifier/companions без обратного маппинга по имени)
	categoryID int
	// categoryName русское название категории
	categoryName string
	// cuisineRU русское название кухни
	cuisineRU string
	// priceMinor цена в копейках
	priceMinor int
	// restrictionReason заполняется ТОЛЬКО для блюд из restricted-блока: короткая
	// причина, по которой блюдо не подходит конкретному гостю («содержит рыбу»,
	// «не вегетарианское»). Для main/companions всегда пусто.
	restrictionReason string
}

// buildPrompt собирает массив llm.Message: system + anchor + recent + user-msg с RAG-контекстом.
// На выходе формат, совместимый с OpenAI chat-completions (используется и OpenRouter, и NVIDIA NIM).
func buildPrompt(in promptInput) []llm.Message {
	out := make([]llm.Message, 0, len(in.history)+3)
	sys := in.systemContent
	if sys == "" {
		sys = systemPromptFallback
	}
	out = append(out, llm.Message{Role: llm.RoleSystem, Content: sys})

	if in.anchor != nil {
		role := mapHistoryRole(in.anchor.Role)
		if role != "" {
			out = append(out, llm.Message{Role: role, Content: in.anchor.Content})
		}
	}

	for i := range in.history {
		m := &in.history[i]
		role := mapHistoryRole(m.Role)
		if role == "" {
			continue
		}
		out = append(out, llm.Message{Role: role, Content: m.Content})
	}

	out = append(out, llm.Message{
		Role:    llm.RoleUser,
		Content: buildUserContent(in),
	})
	return out
}

// mapHistoryRole конвертирует role из БД в llm.Role; system-сообщения из истории игнорируем
func mapHistoryRole(role string) llm.Role {
	switch usecasemodels.MessageRole(role) {
	case usecasemodels.RoleUser:
		return llm.RoleUser
	case usecasemodels.RoleAssistant:
		return llm.RoleAssistant
	}
	return ""
}

// buildUserContent собирает финальный user-prompt: меню-контекст + ID-таблица +
// текущий вопрос + tail-reminder с правилами формата.
//
// Профиль гостя в prompt НЕ включается: hard-фильтры (аллергены, диета) уже применены
// pre-filter'ом в Qdrant; soft-предпочтения добавим позже как отдельный слой, если будет нужно.
//
// id блюд вынесены в отдельный блок «имя → id», а не указаны inline возле названия:
// иначе GigaChat (и не только) копирует pattern «Название (id)» в текст ответа
// («крылышки (6)»), даже если в system явно запрещено писать id в тексте.
//
// Tail-reminder в самом конце сообщения дублирует критичные правила формата
// (bold-обёртка названий, запрет id в тексте, ```json блок). Это лечит «дрейф»
// внимания от system-промпта на длинных диалогах (messages_count > 4-5), когда
// модель перестаёт следовать system и отвечает «как удобно».
func buildUserContent(in promptInput) string {
	var b strings.Builder

	if len(in.retrieved) > 0 {
		b.WriteString("\n=== Меню (наиболее подходящие блюда) ===\n")
		for i, d := range in.retrieved {
			// composition в prompt НЕ кладём: GigaChat-Lite копирует «Состав: …»
			// дословно в ответ (та же болезнь, что была с `[id=6]`). description
			// в БД и так маркетинговая копия состава — для рекомендаций этого
			// достаточно, а на детальные вопросы «что входит в X» в нём же
			// есть ключевые ингредиенты.
			fmt.Fprintf(&b, "\n%d. %s — %s, %s. Цена: %s.\n",
				i+1, d.name, d.categoryName, d.cuisineRU, formatPrice(d.priceMinor))
			if d.description != "" {
				fmt.Fprintf(&b, "   %s\n", d.description)
			}
		}
	} else {
		b.WriteString("\n=== Меню ===\nПо запросу подходящих блюд не найдено.\n")
	}

	if len(in.companions) > 0 {
		b.WriteString("\n=== Сопровождение (на выбор, если уместно к запросу) ===\n")
		for _, d := range in.companions {
			fmt.Fprintf(&b, "- %s (%s) — %s\n",
				d.name, d.categoryName, d.description)
		}
	}

	// ID-таблица: используется ТОЛЬКО для маппинга имени → id в финальном JSON-блоке.
	// Отдельный блок (а не inline возле имени) нужен, чтобы LLM не воспроизводила
	// pattern «название + число» в самом ответе.
	if len(in.retrieved) > 0 || len(in.companions) > 0 {
		b.WriteString("\n=== Соответствие имя → id (только для JSON-блока, в тексте id НЕ упоминать) ===\n")
		for _, d := range in.retrieved {
			fmt.Fprintf(&b, "%s → %d\n", d.name, d.id)
		}
		for _, d := range in.companions {
			fmt.Fprintf(&b, "%s → %d\n", d.name, d.id)
		}
	}

	if len(in.restricted) > 0 {
		b.WriteString("\n=== В меню есть, но НЕ ПОДХОДИТ гостю (аллергии/диета) ===\n")
		for _, d := range in.restricted {
			fmt.Fprintf(&b, "- %s — %s\n", d.name, d.restrictionReason)
		}
		b.WriteString("Эти блюда НЕЛЬЗЯ рекомендовать. Упоминай их ТОЛЬКО если гость прямо спросил про конкретное блюдо из этого списка («у вас есть сибас?»). В этом случае честно ответь: «Да, [блюдо] у нас есть, но вам не подходит, потому что [причина]» — и предложи альтернативу из основного меню. В обычных рекомендациях этот блок игнорируй.\n")
	}

	if len(in.priorRecommended) > 0 {
		fmt.Fprintf(&b, "\n=== Ранее рекомендовано в этом диалоге ===\n%s\n",
			joinInts(in.priorRecommended))
	}

	b.WriteString("\n=== Вопрос гостя ===\n")
	b.WriteString(in.currentUserText)

	// Tail-reminder: дублирует критичные правила формата прямо перед генерацией.
	// Эти строки идут самым последним токеном входа — модель почти всегда им следует,
	// даже если в начале (system) те же правила «утонули» под историей.
	b.WriteString("\n\n--- Напоминание перед ответом (служебное, ПЕРЕОПРЕДЕЛЯЕТ system) ---\n")
	b.WriteString("1. Названия блюд в тексте ВСЕГДА в **двойных звёздочках** (Markdown bold).\n")
	b.WriteString("2. ID блюд (числа) НЕ пиши в тексте — только в финальном JSON-блоке.\n")
	if len(in.restricted) > 0 {
		b.WriteString("3. КРИТИЧНО: блок «НЕ ПОДХОДИТ» — это блюда из реального меню. Если гость прямо назвал блюдо из этого списка — НЕ говори «нет / недоступно / отсутствует». Скажи правду: «Да, [блюдо] у нас есть, но вам не подходит — [причина]» + 1-2 альтернативы из основного «Меню».\n")
		b.WriteString("   Плохо: «К сожалению, сибас нам сейчас недоступен.»\n")
		b.WriteString("   Хорошо: «Да, **Сибас** у нас есть, но вам не подходит — содержит рыбу. Могу предложить **Овощи гриль** или **Овощное рагу по-провански**.»\n")
	}
	b.WriteString("4. Если в «Меню» нет подходящего по запросу — честно: «по вашему запросу подходящего сейчас не нашёл». НЕ предлагай несвязанные блюда из других категорий (бургер вместо рыбы, колбаски вместо сибаса — это галлюцинация, не ответ).\n")
	if in.targetCategoryName != "" {
		// Cross-sell-запрос («гарнир к стейку», «соус к мясу», «десерт после»).
		// Перебиваем общее правило system «узкий запрос → 1-2 варианта» —
		// официант на cross-sell обычно предлагает 2-3 варианта на выбор.
		fmt.Fprintf(&b, "5. Гость спросил конкретно про категорию «%s» — дай ему **2-3 варианта на выбор** из подаваемого «Меню», коротко (1 предложение на каждый), чтобы он мог выбрать. Не ограничивайся одним блюдом.\n", in.targetCategoryName)
		b.WriteString("6. В самом конце ответа ОБЯЗАТЕЛЬНО добавь блок ровно в такой обёртке (тройные backticks):\n")
	} else {
		b.WriteString("5. В самом конце ответа ОБЯЗАТЕЛЬНО добавь блок ровно в такой обёртке (тройные backticks):\n")
	}
	b.WriteString("```json\n")
	b.WriteString(`{"recommended_dish_ids":[<id>, <id>]}` + "\n")
	b.WriteString("```\n")
	b.WriteString("Если рекомендаций нет — пустой массив `[]`. После JSON-блока ничего не пиши.\n")

	return b.String()
}

// formatPrice форматирует цену в копейках в человеческий вид (45000 → "450 ₽")
func formatPrice(minor int) string {
	if minor <= 0 {
		return ""
	}
	return fmt.Sprintf("%d ₽", minor/100)
}

// joinInts собирает []int в строку через запятую
func joinInts(ids []int) string {
	parts := make([]string, len(ids))
	for i, id := range ids {
		parts[i] = fmt.Sprintf("%d", id)
	}
	return strings.Join(parts, ", ")
}
